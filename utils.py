"""przykladowe funkcje utils.py"""


def add(a: int, b: int) -> int:
    """zwraca sume a i b"""
    return a + b


def subtract(a: int, b: int) -> int:
    """zwraca różnice a i b"""
    return a - b


def multiply(a: int, b: int) -> int:
    """zwraca iloczyn a i b"""
    return a * b


def divide(a: int, b: int) -> float:
    """zwraca iloraz a i b"""
    return a / b
